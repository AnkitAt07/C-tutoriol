/*
******Constructor in derived class****** 
>> We can use constructor in derived class.

>>If base class constructor does not have any arguments,
    there is no need of any constructor in derived class.

>>If there are one or more argument in the base class constructor,
    derived class need to pass argument to the base class constructor.

>>If both class have constructor, base class constructor is executed first.
*/

/*
----Constructor in multiple inheritance----
>>In multiple inheritance,base classes are constructed in the order in which they appear in the class declaration.

>>In multiple inheritance,the are constructed executed in the order of inheritance,
*/